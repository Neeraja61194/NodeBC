mutants = ["Professor X", "Cyclops", "Iceman", "Angel", "Beast", "Phoenix"];
console.log("Original Mutants List : ", mutants);
let joinMutants = mutants.join("*");
let sortMutants = mutants.sort();
console.log("Sorted Mutants List :", sortMutants);
let reverseSorted = sortMutants.reverse();
console.log("Reversed Sorted Mutants List : ", reverseSorted);
console.log("Mutants name separated by * : ", joinMutants);